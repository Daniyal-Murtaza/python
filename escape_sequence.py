# To make hello and world written in seperate lines.
print("Hello\nworld")

# To use quotation marks either single or double in the print statement commas.
print("Hello \"world\"")


# To use tab in the string statement.
print("Hello\tworld")

#To use the backspace in the program.
print("Hello \bWorld")

# To use backslash in the program.
print("Hello \\World")
