# floating point division (/)          returns output in floating point
print(4/2)
print(2/4)

# Integer division (//)                returns output in integer form i.e. it do not consider digit after point(.)
print(4//2)
print(2//4)

# modulo (%)                          returns the remainder
print(4%2)
print(13%7)

# Exponent (**)                       returns exponent
print(2**8)

# round function
print(2**0.5)
print(round(2**0.5,4))

# paranthesis then exponent then left to right
# but if the exponents come 2 times in the expression then it will be calculated right to left
