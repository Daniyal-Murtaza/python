# in keyword
# usage of in keyword with if
name = "Daniyal"

#we can use in keyword in several ways like if i want to check a certain character in the string:
# i.e.

if "a" in name:                                # we can also write string replacing name as well here!
    print("a is present in the name")
else:
    print("not present in the name")

# case sensitive!!!
# in keyword is used to check if the given thing is present in the string or not!